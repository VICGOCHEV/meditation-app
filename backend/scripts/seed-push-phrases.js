const PAID = [
  ...TELO.map((text, i) => ({ slot: '08:00', text, order: i })),
  ...SHUM.map((text, i) => ({ slot: '12:00', text, order: i })),
  ...CONTROL.map((text, i) => ({ slot: '16:00', text, order: i })),
  // 20:00 пока повторяет «Тело». Клиент дошлёт вечерние фразы — обновим через CMS.
  ...TELO.map((text, i) => ({ slot: '20:00', text, order: i })),
]

async function main() {
  console.log('Чистим PushPhrase...')
  await db.pushPhrase.deleteMany({})

  console.log(`Сидим ${FREE.length} free + ${PAID.length} paid фраз...`)
  await db.pushPhrase.createMany({
    data: [
      ...FREE.map((p, i) => ({ slot: p.slot, audience: 'free', text: p.text, order: i })),
      ...PAID.map((p) => ({ slot: p.slot, audience: 'paid', text: p.text, order: p.order })),
    ],
  })

  const counts = await db.pushPhrase.groupBy({
    by: ['slot', 'audience'],
    _count: { _all: true },
    orderBy: [{ audience: 'asc' }, { slot: 'asc' }],
  })
  console.log('Итого:')
  counts.forEach((c) =>
    console.log(`  ${c.audience.padEnd(4)} · ${c.slot} → ${c._count._all} фраз`)
  )
  console.log('✓ Готово')
}

main()
  .catch((e) => {
    console.error('FATAL:', e.message)
    process.exit(1)
  })
  .finally(() => db.$disconnect())
