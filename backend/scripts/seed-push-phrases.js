// Сидер фраз пушей. Идемпотентно: при повторных запусках чистит таблицу
// и заливает фразы по новой. Если клиент уже редактировал тексты через
// CMS — повторный запуск ВСЁ ПЕРЕЗАПИШЕТ, не запускай в проде без надо.
//
// Запуск (с прода или локально):
//   cd backend
//   node scripts/seed-push-phrases.js
//
// Контракт сидера:
//   - free: 4 фразы (по одной на слот) — мягкое напоминание о бесплатной
//     практике «Расслабления». Клиент прислал в чате 2026-06-02.
//   - paid: 40 фраз (10 на каждый слот). 3 категории мотивашек из файла
//     клиента «Мотивационные фразы» прокидываются на слоты так:
//       08:00 → «Тело, заземление и настоящность»        (21–30)
//       12:00 → «Внутренний шум и тишина» (хакнуть ум)   (11–20)
//       16:00 → «Иллюзия контроля и гонка за будущим»    (1–10)
//       20:00 → пока повторяем «Тело», вечером симметрично утром.
//               Клиент дошлёт «вечерние» фразы → заменяем через CMS.

import 'dotenv/config'
import { PrismaClient } from '@prisma/client'

const db = new PrismaClient()

