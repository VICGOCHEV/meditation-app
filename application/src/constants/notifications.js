// Удобный helper — все 30 фраз одним массивом, если нужен просто
// случайный выбор без учёта темы.
export const ALL_MOTIVATIONAL_PHRASES = [
  ...MOTIVATIONAL_PHRASES.control,
  ...MOTIVATIONAL_PHRASES.noise,
  ...MOTIVATIONAL_PHRASES.body,
]

export function randomMotivationalPhrase(category) {
  const pool = category
    ? MOTIVATIONAL_PHRASES[category] || ALL_MOTIVATIONAL_PHRASES
    : ALL_MOTIVATIONAL_PHRASES
  return pool[Math.floor(Math.random() * pool.length)]
}
